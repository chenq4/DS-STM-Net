class FMF(nn.Module):
    def __init__(self, channels, img_size,reduction=4):
        super().__init__()
 
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.fc1 = nn.Linear(channels, channels // reduction)
        self.relu = nn.ReLU(inplace=True)
        self.fc2 = nn.Linear(channels // reduction, channels)
        
        self.fc1_2 = nn.Linear(channels, channels // reduction)
        self.fc2_2 = nn.Linear(channels // reduction, channels)
        self.sigmoid = nn.Sigmoid()
        
        self.swin1 = vit_Block(dim=channels,num_heads=8)
        self.swin2 = vit_Block(dim=channels,num_heads=8)
        
        self.dwconv = DWConv(2*channels,ues_se=False) 
        self.conv1x1 = conv1x1(2*channels,channels)
        self.se = SEBlock(channels)
 
    def forward(self, x1,x2):
        b,c,h,w = x1.size()
        out1 = self.avg_pool(x1).view(b, c)
        out2 = self.avg_pool(x2).view(b, c)
   
        out1 = self.fc2(self.relu(self.fc1(out1)))
        out2 = self.fc2_2(self.relu(self.fc1_2(out2)))
        
        out1,out2 = self.sigmoid(out1).view(b, c, 1),self.sigmoid(out2).view(b, c, 1)
        out1 = out1.flatten(2).transpose(1, 2)  # B,1,C
        out2 = out2.flatten(2).transpose(1, 2)  # B,1,C
        x1 = x1.flatten(2).transpose(1, 2)  # B,L,C
        x2 = x2.flatten(2).transpose(1, 2)  # B,L,C

        out2 = torch.cat((x1,out2),dim=1)
        out1 = torch.cat((x2,out1),dim=1)
        
        out2 = self.swin1(out2)[:,:h*w,:]
        out1 = self.swin2(out1)[:,:h*w,:]
        
        out1 = out1.reshape(b, h, w, -1).permute(0, 3, 1, 2).contiguous()
        out2 = out2.reshape(b, h, w, -1).permute(0, 3, 1, 2).contiguous()
        out = self.dwconv(torch.cat((out1,out2),dim=1).flatten(2).transpose(1, 2),h,w).reshape(b, h, w, -1).permute(0, 3, 1, 2).contiguous()
        out = self.se(self.conv1x1(out))
        return out
    
class Ds_StmNet(nn.Module):

    ## Conv 3 + MLP 2 + shifted MLP
    
    def __init__(self,  num_classes, input_channels=3, deep_supervision=False,img_size=224, patch_size=16, in_chans=3,  embed_dims=[ 128, 160, 256],
                 num_heads=[1, 2, 4, 8], mlp_ratios=[4, 4, 4, 4], qkv_bias=False, qk_scale=None, drop_rate=0.,
                 attn_drop_rate=0., drop_path_rate=0., norm_layer=nn.LayerNorm,
                 depths=[1, 1, 1,1,1], sr_ratios=[8, 4, 2, 2,1], **kwargs): # depths和sr我改了 原来是[1,1,1] 和 [8,4,2,1] ，长度不够
        super().__init__()
        
        self.encoder1 = nn.Conv2d(input_channels, 16, 3, stride=1, padding=1)  
        self.encoder2 = nn.Conv2d(16, 32, 3, stride=1, padding=1)  
        self.encoder3 = nn.Conv2d(32, 128, 3, stride=1, padding=1)

        self.ebn1 = nn.BatchNorm2d(16)
        self.ebn2 = nn.BatchNorm2d(32)
        self.ebn3 = nn.BatchNorm2d(128)
        
        self.norm3 = norm_layer(embed_dims[1])
        self.norm4 = norm_layer(embed_dims[2])

#         self.dnorm3 = norm_layer(160)
#         self.dnorm4 = norm_layer(128)

        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]

        self.block1 = nn.ModuleList([DSMLP(
            dim=embed_dims[1], num_heads=num_heads[0], mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[0], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])

        self.block2 = nn.ModuleList([DSMLP(
            dim=embed_dims[2], num_heads=num_heads[0], mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[1], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])

        self.patch_embed3 = OverlapPatchEmbed(img_size=img_size // 4, patch_size=3, stride=2, in_chans=embed_dims[0],
                                              embed_dim=embed_dims[1])
        self.patch_embed4 = OverlapPatchEmbed(img_size=img_size // 8, patch_size=3, stride=2, in_chans=embed_dims[1],
                                              embed_dim=embed_dims[2])

        
        ###################################################################################################################
        embeds_dim=[16,32,128,160,256]
        depths=[2, 2, 2, 2,2]
        num_heads=[2, 4, 16, 20, 32]
        # patch_size 和 window_size 不能随便改，需要注意每一层的尺寸和第一条线要保持一致
        patch_size = 2
        window_size = 8
        self.ape = False
        self.num_layers = len(embeds_dim)
        # split image into non-overlapping patches
        self.patch_embed = PatchEmbed(
            img_size=img_size, patch_size=patch_size, in_chans=in_chans, embed_dim=embeds_dim[0],
            norm_layer=norm_layer)
        num_patches = self.patch_embed.num_patches
        patches_resolution = self.patch_embed.patches_resolution
        self.patches_resolution = patches_resolution

        # absolute position embedding
        if self.ape:
            self.absolute_pos_embed = nn.Parameter(torch.zeros(1, num_patches, embed_dim))
            trunc_normal_(self.absolute_pos_embed, std=.02)

        self.pos_drop = nn.Dropout(p=drop_rate)

        # stochastic depth
        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]  # stochastic depth decay rule

        # build encoder and bottleneck layers
        self.layers = nn.ModuleList()
        
        for i_layer in range(self.num_layers):
            layer = BasicLayer(dim=embeds_dim[i_layer],
                               input_resolution=(patches_resolution[0] // (2 ** i_layer),
                                                 patches_resolution[1] // (2 ** i_layer)),
                               depth=depths[i_layer],
                               num_heads=num_heads[i_layer],
                               window_size=window_size,
                               output_dim = embeds_dim[i_layer+1] if (i_layer < self.num_layers - 1)  else None ,
                               mlp_ratio=mlp_ratios[0],
                               qkv_bias=qkv_bias, qk_scale=qk_scale,
                               drop=drop_rate, attn_drop=attn_drop_rate,
                               drop_path=dpr[sum(depths[:i_layer]):sum(depths[:i_layer + 1])],
                               norm_layer=norm_layer,
                               downsample=PatchMerging if (i_layer < self.num_layers - 1) else None,
                               use_checkpoint=False)
            self.layers.append(layer)
        self.norm = norm_layer(256)
        # end2
        ###################################################################################################################
        self.FMF = FMF(channels=256,img_size=512)
        ###################################################################################################################
        ## skip connetcion    
        self.sc_se1 = SEBlock(channels=160)
        self.sc_se2 = SEBlock(channels=128)
        self.sc_se3 = SEBlock(channels=32)
        self.sc_se4 = SEBlock(channels=16)  
        
        ###################################################################################################################
        
        ## decoder
        self.decoder1_patch_embed = OverlapPatchEmbed(img_size=512, patch_size=3, stride=1, in_chans=256,
                                              embed_dim=256)
        self.decoder1 = nn.ModuleList([DSMLP(
            dim=256, num_heads=8, mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[0], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])
        self.decoder1_1  =  nn.Conv2d(256, 160, 3, stride=1, padding=1) 
        
        self.decoder2_patch_embed = OverlapPatchEmbed(img_size=512, patch_size=3, stride=1, in_chans=160,
                                              embed_dim=160)
        self.decoder2 = nn.ModuleList([DSMLP(
            dim=160, num_heads=8, mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[0], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])
        self.decoder2_2 =  nn.Conv2d(160, 128, 3, stride=1, padding=1)  

        self.decoder3_patch_embed = OverlapPatchEmbed(img_size=512, patch_size=3, stride=1, in_chans=128,
                                              embed_dim=128)
        self.decoder3 = nn.ModuleList([DSMLP(
            dim=128, num_heads=8, mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[0], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])
        self.decoder3_3 =  nn.Conv2d(128, 32, 3, stride=1, padding=1) 
        
        self.decoder4_patch_embed = OverlapPatchEmbed(img_size=512, patch_size=3, stride=1, in_chans=32,
                                              embed_dim=32)
        self.decoder4 = nn.ModuleList([DSMLP(
            dim=32, num_heads=8, mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[0], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])
        self.decoder4_4 =  nn.Conv2d(32, 16, 3, stride=1, padding=1)
        
        self.decoder5_patch_embed = OverlapPatchEmbed(img_size=512, patch_size=3, stride=1, in_chans=16,
                                              embed_dim=16)
        self.decoder5 = nn.ModuleList([DSMLP(
            dim=16, num_heads=8, mlp_ratio=1, qkv_bias=qkv_bias, qk_scale=qk_scale,
            drop=drop_rate, attn_drop=attn_drop_rate, drop_path=dpr[0], norm_layer=norm_layer,
            sr_ratio=sr_ratios[0])])
        self.decoder5_5 =  nn.Conv2d(16, 16, 3, stride=1, padding=1)
        ###################################################################################################################
        # SEBlock
        self.decoder_se1 = SEBlock(channels=160)
        self.decoder_se2 = SEBlock(channels=128)
        self.decoder_se3 = SEBlock(channels=32)
        self.decoder_se4 = SEBlock(channels=16)
        self.decoder_se5 = SEBlock(channels=16)
        ###################################################################################################################
        self.dbn1 = nn.BatchNorm2d(160)
        self.dbn2 = nn.BatchNorm2d(128)
        self.dbn3 = nn.BatchNorm2d(32)
        self.dbn4 = nn.BatchNorm2d(16)
        
        self.final = nn.Conv2d(16, num_classes, kernel_size=1)
        self.final_skip = nn.Conv2d(input_channels, num_classes, kernel_size=1)
        self.soft = nn.Softmax(dim =1)

    def forward(self, x):
        
        B = x.shape[0]#8
        #print("x_shape", x.shape)#x_shape torch.Size([8, 3, 512, 512])
        ### Encoder
        ### Conv Stage
     
        ### Stage 1
        out = self.encoder1(x)
        out = F.relu(F.max_pool2d(self.ebn1(out),2,2))#池化核大小2x2，步幅=2
        t1 = out
        #print("t1_shape", t1.shape)#t1_shape torch.Size([8, 16, 256, 256])
        ### Stage 2
        out = F.relu(F.max_pool2d(self.ebn2(self.encoder2(out)),2,2))
        t2 = out
        #print("t2_shape", t2.shape)#t2_shape torch.Size([8, 32, 128, 128])
        ### Stage 3
        out = F.relu(F.max_pool2d(self.ebn3(self.encoder3(out)),2,2))
        t3 = out
        #print("t3_shape", t3.shape)#t3_shape torch.Size([8, 128, 64, 64])

        ### Tokenized MLP Stage
        ### Stage 4

        out,H,W = self.patch_embed3(out)
        #print("stage4_out_shape: ",out.shape)#stage4_out_shape:  torch.Size([8, 1024, 160])
#         #print("H:,",H ,"W:",W)#H:, 32 W: 32

        for i, blk in enumerate(self.block1):
            out = blk(out, H, W)
        out = self.norm3(out)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
        t4 = out
        #print("t4_shape", t4.shape)#t3_shape torch.Size([8, 128, 64, 64])
        
        ### Bottleneck

        out ,H,W= self.patch_embed4(out)
        #print("5_out_shape: ",out.shape)  #5_out_shape:  torch.Size([8, 256, 256])
            ##print("H:,",H ,"W:",W)            #H:, 16 W: 16
        for i, blk in enumerate(self.block2):
            out = blk(out, H, W)
        out = self.norm4(out)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()

        #print("x:",x.shape)
        out2 = self.patch_embed(x)
        #print("out2:",out2.shape)
        if self.ape:
            out2 = out2 + self.absolute_pos_embed
        out2 = self.pos_drop(out2)
        out2_layers = []
        for layer_i,layer in enumerate(self.layers):
            B,L,C = out2.shape
            H = int(L ** 0.5)
            W = H
            temp = out2.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
            #print("temp.shape",temp.shape)
            out2_layers.append(temp)
            out2 = layer(out2)
        out2 = self.norm(out2)  # B L C
        
        
        B,L,C = out2.shape
        H = int(L ** 0.5)
        W = H
        out2 = out2.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
        
        ############################################################################
#         out = out+out2
        # FMF
        out = self.FMF(out,out2)
        #############################################################################
        ## decoder

        out ,H,W= self.decoder1_patch_embed(out)
        #print("out",out.shape)   
        for i, blk in enumerate(self.decoder1):
            out = blk(out, H, W)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()
        out = F.relu(F.interpolate(self.dbn1(self.decoder_se1(self.decoder1_1(out))),scale_factor=(2,2),mode ='bilinear'))
        out = torch.add(out,self.sc_se1(t4+out2_layers[3]))

        ### Stage 3
        out ,H,W= self.decoder2_patch_embed(out)
        #print("out",out.shape)   
        for i, blk in enumerate(self.decoder2):
            out = blk(out, H, W)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()   
        out = F.relu(F.interpolate(self.dbn2(self.decoder_se2(self.decoder2_2(out))),scale_factor=(2,2),mode ='bilinear'))
        out = torch.add(out,self.sc_se2(t3+out2_layers[2]))

        out ,H,W= self.decoder3_patch_embed(out) 
        for i, blk in enumerate(self.decoder3):
            out = blk(out, H, W)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()   
        out = F.relu(F.interpolate(self.dbn3(self.decoder_se3(self.decoder3_3(out))),scale_factor=(2,2),mode ='bilinear'))
        out = torch.add(out,self.sc_se3(t2+out2_layers[1]))

        out ,H,W= self.decoder4_patch_embed(out)
        for i, blk in enumerate(self.decoder4):
            out = blk(out, H, W)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()   
        out = F.relu(F.interpolate(self.dbn4(self.decoder_se4(self.decoder4_4(out))),scale_factor=(2,2),mode ='bilinear'))
        out = torch.add(out,self.sc_se4(t1+out2_layers[0]))
  
        out ,H,W= self.decoder5_patch_embed(out)
        for i, blk in enumerate(self.decoder5):
            out = blk(out, H, W)
        out = out.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()   
        out = F.relu(F.interpolate(self.decoder5_5(self.decoder_se5(out)),scale_factor=(2,2),mode ='bilinear'))
        #print(out.shape)
        return self.final(out) + self.final_skip(x)
